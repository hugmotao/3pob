#include "functions.h"
#include <math.h>

// Implementação da função para calcular a área de um círculo
float calcularAreaCirculo(float raio) {
    const float PI = 3.14159;
    return PI * raio * raio;
}

// Implementação da função para somar dois números
int somarNumeros(int a, int b) {
    return a + b;
}